import os
os.environ['TF_ENABLE_ONEDNN_OPTS'] = '0'

import cv2
import numpy as np
import mediapipe as mp
from keras.models import load_model

from mogrecorder import draw_landmarks, mediapipe_detection, extract_keypoints

import pickle

with open("data.pkl", "rb") as f:
    loaded_data = pickle.load(f)

actions = loaded_data["actions"]
X_test = loaded_data["X_test"]
y_test = loaded_data["y_test"]
mp_holistic = mp.solutions.holistic # Holistic model
mp_drawing = mp.solutions.drawing_utils # Drawing utilities


model = load_model('action.h5')
res = model.predict(X_test)


from sklearn.metrics import multilabel_confusion_matrix, accuracy_score
yhat = model.predict(X_test)
ytrue = np.argmax(y_test, axis=1).tolist()
yhat = np.argmax(yhat, axis=1).tolist()
multilabel_confusion_matrix(ytrue, yhat)

accuracy_score(ytrue, yhat)

from scipy import stats
colors = [(245,117,16), (117,245,16), (16,117,245)]
def prob_viz(res, actions, input_frame, colors):
    output_frame = input_frame.copy()
    for num, prob in enumerate(res):
        # Ensure prob is a scalar value
        prob_scalar = prob[0] if isinstance(prob, np.ndarray) else prob
        # Multiply prob_scalar by 100
        prob_scaled = int(prob_scalar * 100)
        cv2.rectangle(output_frame, (0,60+num*40), (prob_scaled, 90+num*40), colors[num], -1)
        cv2.putText(output_frame, actions[num], (0, 85+num*40), cv2.FONT_HERSHEY_SIMPLEX, 1, (255,255,255), 2, cv2.LINE_AA)
        
    return output_frame

